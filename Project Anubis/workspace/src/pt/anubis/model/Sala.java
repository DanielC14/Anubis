package pt.anubis.model;


public class Sala {
	
	private String bloco;
	private String sala;

	

	public String getBloco() {
		return bloco;
	}

	public void setBloco(String bloco) {
		this.bloco = bloco;
	}

	public String getSala() {
		return sala;
	}

	public void setSala(String sala) {
		this.sala = sala;
	}

	public Sala(String bloco, String sala) {
		super();
		this.bloco = bloco;
		this.sala = sala;
	}

	
	
	
	

}
